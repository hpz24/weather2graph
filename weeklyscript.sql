SELECT 
    CONCAT(SUBSTRING(TIME,1,15),"0:00")AS TIMEINTER, 
    ROUND(AVG(WERT),2) AS WERT
FROM `1press30`
    WHERE 
YEAR(TIME) = '2015' AND
WEEK(TIME,5) = WEEK('2015-07-17 10:36:31',5)
    GROUP BY SUBSTRING(TIME,1,15)
INTO OUTFILE '/tmp/2015-07-17-press.csv'
	FIELDS TERMINATED BY ','
	LINES TERMINATED BY '\n';
